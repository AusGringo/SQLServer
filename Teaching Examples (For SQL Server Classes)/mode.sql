;WITH CTE AS(
	SELECT *
		, DENSE_RANK() OVER (ORDER BY SA DESC) Frequency
	FROM @kk
)
SELECT TOP 1 SA AS [Mode]
FROM CTE
ORDER BY Frequency DESC
